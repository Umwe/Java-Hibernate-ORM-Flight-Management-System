    public void read(){
        try {
            Registry registry = LocateRegistry.getRegistry("127.0.0.1", 6000);
            AirlineInterface intrf = (AirlineInterface)registry.lookup("Airline");
            List<Airline>airl = intrf.retrieveAirline();
            for(Airline air:airl){
                airltable.addRow(new Object[]{
                    air.getId(),
                    air.getContact(),
                    air.getName()
                });
            }
            airlinetable.setModel(airltable);
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

