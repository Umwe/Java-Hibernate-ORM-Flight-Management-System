    private void insertbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertbtnActionPerformed
        // TODO add your handling code here:
        try {
           
            if(AIrlineidtxt.getText().trim().isEmpty() ||airlinename.getText().trim().isEmpty() || airlinecontact.getText().trim().isEmpty() ){
                JOptionPane.showMessageDialog(this, "Please provide all information","Data Required",JOptionPane.ERROR_MESSAGE);
            }else{
                Model.Airline pst = new Model.Airline();
                pst.setId(Integer.parseInt(AIrlineidtxt.getText()));
                pst.setName(airlinename.getText());
                pst.setContact(airlinecontact.getText());
                
                Registry registry = LocateRegistry.getRegistry("127.0.0.1", 6000);
                AirlineInterface intrf = (AirlineInterface)registry.lookup("Airline");
                String feedback = intrf.registerairline(pst);
                JOptionPane.showMessageDialog(this, feedback,"Successfully Inserted",JOptionPane.INFORMATION_MESSAGE);
                airltable.setRowCount(0);
                read();
            }
            
        } catch (Exception e) {
        e.printStackTrace();
        }
    }//GEN-LAST:event_insertbtnActionPerformed

